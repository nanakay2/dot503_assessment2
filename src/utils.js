export function addNumbers(firstNumber, secondNumber) {
    return firstNumber + secondNumber
}

export function subtractTwoNumbers(firstNumber, secondNumber) {
    return firstNumber - secondNumber
}

export function multiplyTwoNumbers(firstNumber, secondNumber) {
    return firstNumber * secondNumber
}

export function divideTwoNumbers(firstNumber, secondNumber) {
    return firstNumber / secondNumber
}